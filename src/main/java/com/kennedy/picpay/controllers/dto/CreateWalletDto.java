package com.kennedy.picpay.controllers.dto;

import com.kennedy.picpay.entities.Wallet;
import com.kennedy.picpay.entities.WalletType;

public record CreateWalletDto(String fullName,
                              String cpfCnpj,
                              String email,
                              String password,
                              WalletType.Enum walletType) {
    public Wallet toWallet() {
        return new Wallet(
                fullName,
                cpfCnpj,
                email,
                password,
                walletType.get()
        );
    }
}
